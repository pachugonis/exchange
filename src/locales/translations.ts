// Type for nested translation keys
type TranslationKey = string;

// Helper function to get translation
export const getTranslation = (
  key: TranslationKey,
  locale: Locale
): string => {
  const keys = key.split('.');
  let value: any = translations;

  // Navigate to the category first (e.g., 'navigation')
  const category = keys[0];
  if (!(category in value)) {
    console.warn(`Translation category not found: ${category}`);
    return key;
  }

  value = value[category];

  // Check if locale exists in this category
  if (!(locale in value)) {
    console.warn(`Locale not found in category ${category}: ${locale}`);
    return key;
  }

  // Get the localized object
  value = value[locale];

  // Navigate through remaining keys (e.g., 'home' or 'steps.step1')
  for (let i = 1; i < keys.length; i++) {
    const k = keys[i];
    if (value && typeof value === 'object' && k in value) {
      value = value[k];
    } else {
      console.warn(`Translation key not found: ${key} at ${k}`);
      return key;
    }
  }

  // Return the final string value
  if (typeof value === 'string') {
    return value;
  }

  console.warn(`Translation not a string for key: ${key}, locale: ${locale}`);
  return key;
};
